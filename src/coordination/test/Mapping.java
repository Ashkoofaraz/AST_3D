package coordination.test;

public class Mapping {

	public int agent;
	public int position;

	public Mapping(int agent, int position) {

		this.agent = agent;
		this.position = position;

	}

	public int getAgent() {
		return agent;
	}

	public void setAgent(int agent) {
		this.agent = agent;
	}

	public int getPosition() {
		return position;
	}

	public void setPosition(int position) {
		this.position = position;
	}

}
